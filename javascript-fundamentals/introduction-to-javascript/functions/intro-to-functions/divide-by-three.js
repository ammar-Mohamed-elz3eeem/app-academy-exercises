/**
 * This function returns the passed in number 
 * argument divided by three.
 *
 * @param {number} num
 *     This is the number to divide by 3.
 *
 * @returns
 *     return the given string divided by 3.
 */
function divideByThree(num) {
  return num / 3;
}

console.log(divideByThree(9)); // => 3
console.log(divideByThree(15)); // => 5
console.log(divideByThree(98)); // => 32.666666666666664