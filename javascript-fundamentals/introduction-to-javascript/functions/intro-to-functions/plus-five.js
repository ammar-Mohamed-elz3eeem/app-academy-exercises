/**
 * This function takes in a number as an argument
 * and returns the sum of that number and 5.
 *
 * @param {number} num
 *     This is the number to add 5 to.
 * 
 * @returns
 *     the result of adding 5 to the given number 
 */
function plusFive (num) {
  return num + 5;
}

console.log(plusFive(10)); // => 15
console.log(plusFive(2)); // => 7
console.log(plusFive(-8)); // => -3
