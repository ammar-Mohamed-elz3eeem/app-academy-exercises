/**
 * This function takes in a string and prints out
 * "Hello, " followed by the string.
 *
 * @param {string} str
 *     This is the string to prepend with "Hello, ".
 */
function hello (str) {
	console.log(`Hello, ${str}`);
}

hello("cat"); // prints "Hello, cat"
hello("Helen"); // prints "Hello, Helen"
hello("Sting"); // prints "Hello, Sting"
